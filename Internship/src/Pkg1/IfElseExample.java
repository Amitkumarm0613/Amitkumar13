package Pkg1;
// odd and even number
public class IfElseExample 
{

	public static void main(String[] args) 
	{
		int number =16;
		// check if the number is divided by 2 or not
		if(number%2==0)
			// 16%2=8
		{
			System.out.println("number is even");
		}
		else
		{
			System.out.println("number is odd");
		}

	}

}
